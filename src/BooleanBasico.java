public class BooleanBasico {

    // verificando Boolean

    public static void main(String[] args) {
        Boolean verificar1 = 1 > 0;
        Boolean verificar2 = 1 < 0;
        Boolean verificar3 = 1 == 0;
        Boolean verificar4 = 1 != 0;
        System.out.println(verificar1);
        System.out.println(verificar2);
        System.out.println(verificar3);
        System.out.println(verificar4);
    }
}
