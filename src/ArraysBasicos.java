public class ArraysBasicos {
    public static void main(String[] args) {

        //criando um Array

        int vetor1[] = {8, 6, 7, 95, 55, 47, 90, 11}; // tipo inteiro
        String vetor2[] = {"Arthur", "Karla", "Victoria", "Vital", "Alba"};

        //tamanho total do Array
        System.out.println("Exemplos Inteiro");
        System.out.println("Comprimento do Array: "+vetor1.length);

        //imprimindo o Array por indice
        System.out.println("Array por indice: "+vetor1[3]);
        System.out.println("-------------------------- >");


        //tamanho total do Array
        System.out.println("Exemplos String");
        System.out.println("Comprimento do Array: "+vetor2.length);

        //imprimindo o Array por indice
        System.out.println("Array por indice: "+vetor2[2]);



    }
}
